# Assignment Operators
a = 10

# Simple Assignment
b = a
print(b)

# Addition Assignment
a += 5
print(a)

# Subtraction Assignment
a -= 3
print(a)

# Multiplication Assignment
a *= 2
print(a)

# Division Assignment
a /= 4
print(a)

# Modulus Assignment
a %= 5
print(a)

# Exponentiation Assignment
a **= 3
print(a)

# Floor Division Assignment
a //= 2
print(a)