# Comparison Operators
a = 10
b = 5

# equal to
result = a == b
print(result)

# not equal to
result = a != b
print(result)

# greater than
result = a > b
print(result)  

# less than
result = a < b
print(result) 

# greater than or equal to
result = a >= b
print(result)

# less than or equal to
result = a <= b
print(result)
