# Arithmetic Operators
a = 10
b = 3

# Addition
result = a + b
print(result) 

# Subtraction
result = a - b
print(result)

# Multiplication
result = a * b
print(result)

# Division
result = a / b
print(result)

# Floor Division
result = a // b
print(result)

# Modulus
result = a % b
print(result)

# Exponentiation
result = a ** b
print(result) 
